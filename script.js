  function getOrCreateGroupPanel(groupKey) {
    return (
      groupPanelsLayer.querySelector(`.group-panel[data-group-key="${groupKey}"]`) ||
      createGroupPanel(groupKey)
    );
  }

  function positionGroupPanel(panel, groupKey, occupiedRects = []) {
    const info = groupInfoMap[groupKey];
    const members = getGroupMembers(groupKey);

    const title = panel.querySelector(".group-panel-title");
    const desc = panel.querySelector(".group-panel-desc");

    title.textContent = info?.title || `Group ${groupKey}`;
    desc.textContent = info?.description || "";

    const anchorIndex = Math.floor(members.length / 2);
    const anchorCard = getCardById(members[anchorIndex]);
    if (!anchorCard) return null;

    const gap = 18;
    const margin = 20;

    panel.hidden = false;
    panel.style.left = "0px";
    panel.style.top = "0px";

    const panelWidth = panel.offsetWidth || 260;
    const panelHeight = panel.offsetHeight || 160;

    const person = getPersonByCard(anchorCard);
    const anchorLeft = person.x;
    const anchorTop = person.y;

    const candidatePositions = [
      { left: anchorLeft + CARD_SIZE + gap, top: anchorTop },
      { left: anchorLeft - panelWidth - gap, top: anchorTop },
      { left: anchorLeft, top: anchorTop - panelHeight - gap },
      { left: anchorLeft, top: anchorTop + CARD_SIZE + gap }
    ];

    function clampPanelPosition(pos) {
      return {
        left: clamp(pos.left, margin, WORLD_WIDTH - panelWidth - margin),
        top: clamp(pos.top, margin, WORLD_HEIGHT - panelHeight - margin)
      };
    }

    function overlapsOccupied(rect) {
      return occupiedRects.some((other) => {
        const separated =
          rect.left + rect.width < other.left ||
          other.left + other.width < rect.left ||
          rect.top + rect.height < other.top ||
          other.top + other.height < rect.top;

        return !separated;
      });
    }

    let chosen = null;

    for (const pos of candidatePositions) {
      const clampedPos = clampPanelPosition(pos);
      const rect = {
        left: clampedPos.left,
        top: clampedPos.top,
        width: panelWidth,
        height: panelHeight
      };

      if (!overlapsOccupied(rect)) {
        chosen = rect;
        break;
      }
    }

    if (!chosen) {
      const fallback = clampPanelPosition(candidatePositions[0]);
      chosen = {
        left: fallback.left,
        top: fallback.top,
        width: panelWidth,
        height: panelHeight
      };
    }

    panel.style.left = `${chosen.left}px`;
    panel.style.top = `${chosen.top}px`;

    return chosen;
  }

  function renderGroupPanels(activeGroups) {
    const activeSet = new Set(activeGroups);

    [...groupPanelsLayer.querySelectorAll(".group-panel")].forEach((panel) => {
      const groupKey = panel.dataset.groupKey;
      panel.hidden = !activeSet.has(groupKey);
    });

    const occupiedRects = [];

    activeGroups.forEach((groupKey) => {
      const panel = getOrCreateGroupPanel(groupKey);
      const rect = positionGroupPanel(panel, groupKey, occupiedRects);
      if (rect) occupiedRects.push(rect);
    });
  }

  function updateMinimapLabel() {
    if (!minimapLabel) return;
    const matched = permanentMatched.size;
    const total = people.length;
    minimapLabel.textContent = `${matched} / ${total} matched`;
  }

  function updateLiveGroupPanel() {
    const uniqueGroups = [...new Set(Object.values(groupMap))];
    const activeGroups = [];

    for (const groupKey of uniqueGroups) {
      if (isGroupFullyAssembled(groupKey)) {
        activeGroups.push(groupKey);

        const members = getGroupMembers(groupKey);
        members.forEach((id) => permanentMatched.add(id));
      }
    }

    applyPermanentMatched();
    renderGroupPanels(activeGroups);
    updateMinimapDots();
    updateMinimapMatches(activeGroups);
    updateMinimapViewport();
    updateMinimapLabel();
  }

